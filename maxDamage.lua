end
local function file_exists(name)
  local f=io.open(name,"r")
  if f~=nil then io.close(f) return true else return false end
end
local function starts_with(str, start)
   return str:sub(1, #start) == start
end
local function main()
  local mods = {}
  for i,j in pairs(mD) do
    print(i)
    local mod = convert.TextToItem(i).mod
    if mods[mod] == nil then
      mods[mod] = 1
    else
      mods[mod] = mods[mod] + 1
    end
  end
  for a,b in pairs(mods) do
    local file = io.open("src/max/" .. a .. ".lua","w")
    local counter = 1
    file:write("return {\n")
    for i,j in pairs(mD) do
      if starts_with(i, a) then
        if counter == b then
          file:write(i .. "=" .. j .. "\n")
        else
          file:write(i .. "=" .. j .. ",\n")
        end
        counter = counter + 1
      end
    end
    file:write("}")
    file:close()
  end
end
